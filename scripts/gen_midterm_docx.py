HEI = '\u9ed1\u4f53'
SONG = '\u5b8b\u4f53'

def set_cn(run, family=SONG):
    run.font.name = family
    run.element.rPr.rFonts.set(qn('w:eastAsia'), family)

def add_title(text, level=0):
    sizes = {0: 22, 1: 16, 2: 14}
    heading_level = {0: 1, 1: 2, 2: 3}
    p = doc.add_heading(text, level=heading_level[level])
    if level == 0:
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for run in p.runs:
        run.font.size = Pt(sizes[level])
        set_cn(run, HEI)

def add_para(text):
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Pt(24)
    run = p.add_run(text)
    set_cn(run)
    run.font.size = Pt(12)

def add_info(label, value):
    p = doc.add_paragraph()
    r1 = p.add_run(label)
    r1.bold = True
    r1.font.size = Pt(12)
    set_cn(r1)
    r2 = p.add_run(value)
    r2.font.size = Pt(12)
    set_cn(r2)


